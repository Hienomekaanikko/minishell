#include "libft.h"

char	*ft_strjoin_free(char *s1, char *s2)
{
	char	*result;

	result = ft_strjoin(s1, s2);
	if (!result)
	{
		free(s1);
		return (NULL);
	}
	free(s1);
	return (result);
}
